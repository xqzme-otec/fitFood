def emoji_for(name: str, category: str | None = None) -> str:
    """Эмодзи продукта: сначала по названию, иначе — по категории, иначе запасной."""
    cleaned = clean_title(name)
    for keyword, symbol in PRODUCT_EMOJI.items():
        if keyword in cleaned:
            return symbol
    if category and category in CATEGORY_EMOJI:
        return CATEGORY_EMOJI[category]
    return DEFAULT_EMOJI
