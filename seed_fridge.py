def main():
    Base.metadata.create_all(bind=engine)
    db = SessionLocal()
    try:
        user = db.query(User).filter(User.email == EMAIL).first()
        if not user:
            print(f"[!] Пользователь {EMAIL} не найден")
            return

        # Очищаем холодильник пользователя
        deleted = db.query(FridgeItem).filter(FridgeItem.user_id == user.id).delete()
        db.commit()
        print(f"[~] Удалено {deleted} старых записей холодильника")

        # Загружаем все продукты из каталога для матчинга по имени
        all_products = {p.name: p for p in db.query(Product).all()}

        today = date.today()
        added = 0
        no_match = []

        for name, category, qty, unit, days, cal, prot, fat, carbs in FRIDGE_ITEMS:
            # Ищем продукт в каталоге — сначала точное совпадение
            product = all_products.get(name)

            if product is None:
                # Если не нашли — создаём новый продукт в каталоге с нашими КБЖУ
                product = Product(
                    name=name,
                    category=category,
                    unit=unit if unit in ("g", "ml") else "g",
                    calories=cal,
                    protein=prot,
                    fat=fat,
                    carbs=carbs,
                )
                db.add(product)
                db.flush()
                all_products[name] = product
                no_match.append(name)

            db.add(FridgeItem(
                user_id=user.id,
                product_id=product.id,
                name=name,
                category=category,
                quantity=qty,
                unit=unit,
                expiry_date=today + timedelta(days=days),
            ))
            added += 1

        db.commit()
        print(f"[ok] Добавлено {added} продуктов в холодильник {EMAIL}")
        if no_match:
            print(f"[+] Создано {len(no_match)} новых продуктов в каталоге: {', '.join(no_match[:5])}{'...' if len(no_match) > 5 else ''}")
    finally:
        db.close()


if __name__ == "__main__":
    main()
